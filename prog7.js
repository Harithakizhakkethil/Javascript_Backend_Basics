//operators

//addition
var x = 10
var y = 20
console.log(`sum of ${x} and ${y} is : ${x+y}`);

//subtraction
var x1 = 10
var y1 = 20
console.log(`differenced of ${x1} and ${y1} is : ${x1-y1}`);

//multiplication
var x2 = 10
var y2 = 20
console.log(`product of ${x2} and ${y2} is : ${x2*y2}`);

//division
var x3 = 10
var y3 = 20
console.log(`quotient of ${x3} and ${y3} is : ${x3/y3}`);

//modules
var x4 = 10
var y4 = 20
console.log(`remainder of ${x4} and ${y4} is : ${x4%y4}`);

//exponential/power
var x5 = 10
var y5 = 2
console.log(`${x5} power ${y5} is : ${x5**y5}`);


// * Relational operators
// == and ===
 num1 = 10
 num2 = '10'
  console.log(`${num1} and ${num2} is equal? ${num1==num2}`);
  console.log(`${num1} and ${num2} is equal? ${num1===num2}`);

// > , >= . < , <=
num1 = 10
num2 = 50
  console.log(`${num1} is greater than ${num2}  ${num1>num2}`);
  console.log(`${num1} is less than ${num2}  ${num1<num2}`);
  console.log(`${num1} is greater than or equal to ${num2}  ${num1>=num2}`);
  console.log(`${num1} is less than or equal to ${num2}  ${num1<=num2}`);

//logical operators

a = 10
console.log(`5<a<20 : ${5<a && a<20}`); //true
console.log(`5<a<20 : ${5<a || a<20}`); //true

a=30
console.log(`5<a<20 : ${5<a && a<20}`); //false
console.log(`5<a<20 : ${5<a || a<20}`); //true


//Increment(++) and Decrement operators(--)
